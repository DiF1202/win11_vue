// 对每一个图片进行处理
const loader = url => {
    return new Promise((resolve,reject)=>{
        const image = new Image();
        image.onload = ()=> resolve();
        image.onerror = ()=> reject();
        image.src = url;
    })
}

// 返回 promise.all() 对象
const preLoad = ()=>{
    const promiseArr = [];
    imgs.forEach(src=>{
        promiseArr.push(loader(src));
    })
    return Promise.all(promiseArr);
}

export default preLoad